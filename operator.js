// Arithmetic operator
let a=10;
let b=20;
console.log("Add:",a+b);
console.log("Sub:",a-b);
console.log("Mul:",a*b);
console.log("Div:",a/b);
console.log("Mod:",a%b);

//Tricky
console.log(a+b+"Hi");
console.log("Hi"+a+b);
console.log("20"-"10");
console.log("20"*"10");
console.log("20"/"2");
console.log("20"/2);
console.log(30/"Hi");//Not a Number

//Assignment operator(Comparsion Operators)

let c=20; //literal syntax
console.log(b==c);
console.log(b===c);

let d=new Number(20);//Object syntax
console.log(b==d);
console.log(b===d);
 